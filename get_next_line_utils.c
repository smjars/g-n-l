/*Esta función concatena dos cadenas s1 y s2.
Si s1 es NULL, se inicializa como una cadena vacía.
Reserva memoria suficiente para la nueva cadena concatenada.
Copia s1 y luego s2 en la nueva cadena, libera la memoria de s1 y retorna la nueva cadena concatenada.*/
char	*ft_strjoin(char *s1, char *s2)
{
	char	*str;
	size_t	i;
	size_t	c;

	if (!s1)
	{
		s1 = malloc(sizeof(char) + 1);
		if (!s1)
			return (0);
		s1[0] = 0;
	}
	str = (char *)malloc(sizeof(char) * ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!str)
		return (ft_free(&s1));
	i = -1;
	while (s1[++i])
		str[i] = s1[i];
	c = -1;
	while (s2[++c])
		str[i + c] = s2[c];
	str[i + c] = '\0';
	free(s1);
	return (str);
}

/*Calcula y retorna la longitud de una cadena s.
Si s es NULL, retorna 0.*/
size_t	ft_strlen(char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
		i++;
	return (i);
}

/*Busca el carácter c en la cadena s.
Retorna un puntero a la primera ocurrencia de c en s, o NULL si no se encuentra.*/
char	*ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return (&((char *)s)[i]);
		i++;
	}
	if ((char)c == '\0')
		return (&((char *)s)[i]);
	return (0);
}

/*Crea una subcadena de s que comienza en start y tiene una longitud de len.
Si start está fuera de los límites de la cadena s, retorna una cadena vacía.
Reserva memoria suficiente para la subcadena y la copia desde s.
Retorna la subcadena creada.*/
char	*ft_substr(char *s, unsigned int start, size_t len)
{
	size_t	i;
	char	*res;

	i = 0;
	if (!s)
		return (0);
	if (start > ft_strlen(s))
	{
		res = malloc(sizeof(char) * (1));
		if (!res)
			return (NULL);
		res[0] = '\0';
		return (res);
	}
	if (ft_strlen(s) - start < len)
		len = ft_strlen(s) - start;
	res = malloc(sizeof(char) * (len + 1));
	if (!res)
		return (NULL);
	while (start < ft_strlen(s) && i < len && s[start])
		res[i++] = s[start++];
	res[i] = '\0';
	return (res);
}
